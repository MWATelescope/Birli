/// ```python
/// #!/usr/bin/env python3
/// import sys
/// import numpy as np
///
/// if len(sys.argv) != 2:
///     print("Must supply the freq resolution in kHz as first argument (Must be a multiple of 0.2kHz)")
///
/// Fs = float(sys.argv[1])  #Fine Channel Resolution in kHz
/// if np.mod(Fs*5,1) != 0:
///     print("Fine channel resolution must be a multiple of 200Hz")
///     exit()
///
/// c  = int(327680/Fs)
/// o  = int(640/Fs)
///
/// h = np.loadtxt('MwaPfbProtoFilterCoeff2009_512x8.dat')
/// H = np.fft.fftshift(np.fft.fft(h,2*c))[c-4*o:c+4*o]
/// H = H / np.max(np.abs(H))
///
/// # --- Compute Power Gain & Aliased Version ---
/// H_power_gain = np.abs(H)**2
/// H_power_aliased = H_power_gain + np.roll(H_power_gain,2*o) + np.roll(H_power_gain,-2*o)
///
/// # --- Slice out the meaningfull region ---
/// H_power_aliased = H_power_aliased[3*o:-3*o]
///
/// # --- Compute the voltage gains from the power gains
/// H_voltage_aliased = np.sqrt(H_power_aliased)
///
/// # --- Save to stdout as txt ---
/// np.savetxt( sys.stdout.buffer, H_voltage_aliased )
/// ```
///
/// run with
///
/// ```bash
/// python compute_pfb_aliased_bandpass.py 0.2 > gains_200Hz.csv
